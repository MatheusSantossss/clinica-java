    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:
        if(txtNome.getText().equals("") || txtMatricula.getText().equals("") || new String(txtSenhaCad.getPassword()).equals("") ){
            JOptionPane.showMessageDialog(null, "Preencha os campos para cadastro!");
        }else if ((new String(txtSenhaCad.getPassword()).equals(new String(txtConfirmSenhaCad.getPassword())))){
            usuario u = new usuario();
            UsuarioDAO dao = new UsuarioDAO();
            
            
            u.setNome(txtNome.getText());
            u.setMatricula(txtMatricula.getText());
            u.setSenha(new String(txtSenhaCad.getPassword()));
            u.setCargo(cbCargo.getSelectedItem().toString());
            u.setChave(new String(txtChaveGerente.getPassword()));
            dao.create(u);
            frCadastro.setVisible(false);
            
        }else{
            JOptionPane.showMessageDialog(null, "Senhas não coincidem!");
        }
    }//GEN-LAST:event_jButton3ActionPerformed


    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        UsuarioDAO dao = new UsuarioDAO();
        
        
        
        if(dao.checkLogin(txtLogin.getText(), new String(txtSenha.getPassword()))){
            
            JOptionPane.showMessageDialog(null, "Bem vindo " + dao.checkUsuario(txtLogin.getText()));
            this.dispose();
        }else{
            JOptionPane.showMessageDialog(null, "Senha incorreta!");
        }
        if(dao.getcargo().equals("Administrador")){
            TelaPrincipal.getMnControle().setVisible(false);
            TelaPrincipal.getMnIAdmin().setVisible(false);
            ((DefaultComboBoxModel) TelaPrincipal.getCbUsr().getModel()).removeElementAt(1);
        }else if(dao.getcargo().equals("Recepcionista")){
            
            TelaPrincipal.getMnClientes().setEnabled(false);
            TelaPrincipal.getMnUsuarios().setEnabled(false);
        }
        
    }//GEN-LAST:event_jButton1ActionPerformed

